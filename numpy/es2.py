import numpy as np

np.random.seed(42)
float_array = np.random.rand(20)
print("float_array:", float_array)
print("float_array over 0.7:", float_array[float_array > 0.7])
float_array[float_array < 0.3] = 0
print("new float_array:", float_array)
print("new float_array, amount of 0:", np.sum(float_array == 0))
